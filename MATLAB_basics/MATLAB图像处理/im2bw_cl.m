I=imread('zy.tif');
% x=rgb2gray(I);
thresh = graythresh(I);     %自动确定二值化阈值
I2 = im2bw(I,thresh); 
figure(1);
subplot(1,2,1);
imshow(I);
title('原始图像');
subplot(1,2,2);
imshow(I2);
title('二值化图像'); 